weather = "dust storm"
case (weather)
when "sunny"
  puts "Woohoo! Sunglasses and flip flops, baby!"
when "cloudy"
  puts "Meh. Carry an umbrella just in case."
when "foggy"
  puts "Throw on a reflective vest."
when "rainy"
  puts "Put on them rubber boots."
when "snowy"
  puts "Dust off the parka."
when "dust storm"
  puts "Are you kidding? Stay inside!"
else
  puts "Are you sure that's an actual weather condition?"
end
